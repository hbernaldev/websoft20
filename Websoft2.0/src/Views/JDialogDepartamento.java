    private void mostrarTabla(){
        
        String[] columnNames = {"Nombre", "Especialidad", "Cantidad Trabajadores"};
        ArrayList<Departamento> departamentos = _service.getDepartamentos();
        
        DefaultTableModel model = new DefaultTableModel(columnNames, departamentos.size());
        
        int row = 0;
        for(Departamento d: departamentos){
            model.setValueAt(d.obtenerNombre(), row, 0);
            model.setValueAt(d.obtenerEspecialidad(), row, 1);
            model.setValueAt(d.obtenerCantidadTrabajadores(), row++, 2);
        }
        
        jTable1.setModel(model);
    }
    
    private void toogleInsertPanel(boolean status){
        nombreTextField.setEnabled(status);
        especialidadTextField.setEnabled(status);
        cantidadSpinner.setEnabled(status);
        saveButton.setEnabled(status);
        cancelButton.setEnabled(status);
        if (!status) {
            resetInsertFormValues();
        } 
    }
    
    private void resetInsertFormValues(){
        nombreTextField.setText("");
        especialidadTextField.setText("");
        cantidadSpinner.setValue(0);
    }

